# Tuple is another data type that allows multiple values and can be different datatypes (immutable - we cannot update the values)
# By declaration we can easily understand because tuple values declare inside normal brackets

values = (1, 2, 3, "babu", 0.9, "dia")
print(values) # output = (1, 2, 3, 'babu', 0.9, 'dia')

# print by index values
print(values[1])  #output = 2

